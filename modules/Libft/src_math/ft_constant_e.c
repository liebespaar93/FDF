double	ft_constant_e(int repeat)
{
	double	e;
	double	temp;
	int		num;

	e = 1;
	temp = 1;
	num = 0;
	while (num < repeat)
	{
		e += 1.0 / temp;
		temp *= num;
		num++;
	}
	return (e - 1);
}
