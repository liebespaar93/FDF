double	ft_constant_pi(int repeat)
{
	double	pi;
	double	temp;
	double	p;
	int		num;

	pi = 1;
	temp = 1;
	p = -1;
	num = 0;
	while (num < repeat)
	{
		p *= -1;
		pi += p * 1.0 / temp;
		temp += 2;
		num++;
	}
	return (4 * (pi - 1));
}
